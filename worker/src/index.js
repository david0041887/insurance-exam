const EXPLAIN_TOOL = {
  name: 'explain_question',
  description: '回傳選擇題解析',
  input_schema: {
    type: 'object',
    properties: {
      explanation: { type: 'string', description: '解釋正確答案原因（2-4句，依台灣保險法及相關規定）' },
      keyLaw:      { type: 'string', description: '相關法條或重要概念，如「保險法第64條」，可留空' },
      wrongNote:   { type: 'string', description: '最容易混淆的錯誤選項及原因（1-2句），可留空' },
    },
    required: ['explanation'],
  },
};

async function explainQuestion(question, options, answer, apiKey) {
  const optText = Object.entries(options).map(([k,v]) => '(' + k + ') ' + v).join('\n');
  const userMsg = '題目：' + question + '\n\n選項：\n' + optText + '\n\n正確答案：' + answer + '\n\n請呼叫 explain_question 工具。';
  const res = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: { 'x-api-key': apiKey, 'anthropic-version': '2023-06-01', 'content-type': 'application/json' },
    body: JSON.stringify({
      model: MODEL, max_tokens: 600, system: EXPLAIN_SYSTEM,
      tools: [EXPLAIN_TOOL], tool_choice: { type: 'tool', name: 'explain_question' },
      messages: [{ role: 'user', content: userMsg }],
    }),
  });
  if (!res.ok) throw new Error('API error ' + res.status + ': ' + await res.text());
  const data = await res.json();
  const toolUse = (data.content||[]).find(b => b.type === 'tool_use');
  if (!toolUse) throw new Error('Model did not call explain_question');
  return toolUse.input;
}

